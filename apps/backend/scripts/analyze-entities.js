function parseSearchResult(result) {
    const filePaths = new Set();
    const lines = result.trim().split('\n');
    for (const line of lines) {
        if (line.startsWith('File: ') || line.startsWith('backend/')) {
            const filePath = line.split(':')[0].replace('File: ', '').trim().replace(/\\/g, '/');
            // The file paths from search result are relative to the project root, let's construct the full path
            filePaths.add(path.join(backendRootPath, '..', filePath));
        }
    }
    return Array.from(filePaths);
}

function getEntityTableName(fileContent) {
    const entityDecoratorMatch = fileContent.match(/@Entity\s*\(([^)]*)\)/);
    if (entityDecoratorMatch) {
        const decoratorArgs = entityDecoratorMatch[1].trim();
        if (decoratorArgs) {
            // Handles @Entity('table_name') or @Entity({ name: 'table_name' })
            const nameMatch = decoratorArgs.match(/(?:'|")([^'"]+)(?:'|")/) || decoratorArgs.match(/name:\s*'(.*?)'/);
            if (nameMatch && nameMatch[1]) {
                return nameMatch[1];
            }
        }
    }
    
    const classNameMatch = fileContent.match(/export\s+class\s+([^{\s]+)/);
    if (classNameMatch) {
        // Applying a simple snake_case conversion for class names
        const className = classNameMatch[1];
        return className.replace(/[A-Z]/g, (letter, index) => {
            return index === 0 ? letter.toLowerCase() : '_' + letter.toLowerCase();
        });
    }
    return null;
}


function getSqlTableNames(sqlContent) {
    const tableNames = new Set();
    const createTableRegex = /CREATE TABLE IF NOT EXISTS `sollus`\.`([^`]+)`/gi;
    let match;
    while ((match = createTableRegex.exec(sqlContent)) !== null) {
        tableNames.add(match[1].toLowerCase());
    }
    return Array.from(tableNames);
}

